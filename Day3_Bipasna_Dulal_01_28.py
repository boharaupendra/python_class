#List of Variables:
#There are two types of variables:
# 1- Local Variable
# 2- Global variable

#int (signed integers)= Int, or integer, is a whole number, positive or negative, without decimals, of unlimited length.
#Int is immutable.

#float (floating point real values)= Float, or "floating point number" is a number, positive or negative, containing one or more decimals.
#Float can also be scientific numbers with an "e" to indicate the power of 10.
#Float is immutable.

#complex (complex numbers)= Complex numbers are written with a "j" as the imaginary part.
# Complex is immutable.

#string= Strings in python are surrounded by either single quotation marks, or double quotation marks.
#'hello' is the same as "hello".
#Strings are immutable.

#list= Lists are used to store multiple items in a single variable.
