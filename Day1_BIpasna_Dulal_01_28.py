#List of identifier.
#Identifier should always start with non- numeric character.
#Identifier shouldnot have spaces between two or more words.
#For example: 

first_name = "Bipasna"      #snake case

second_name = "Frank"

print(first_name, type(first_name))

print(second_name, type(second_name))

number1= 10
number2= 30

print(number1, type(number1))

print(number2, type(number2))

#Identifier is also used to identify symbolic constant.
#for example:

PI= 3.14

print(PI, type(PI))

GRAVITY= 9.8
print(GRAVITY, type(GRAVITY))

# Pascal case
AnimalName= "cat"
print(AnimalName, type(AnimalName))

#Camel case

nameOfThePlace= "Kathmandu"
print(nameOfThePlace, type(nameOfThePlace))




